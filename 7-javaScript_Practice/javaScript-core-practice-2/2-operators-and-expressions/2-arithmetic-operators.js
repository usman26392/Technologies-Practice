/**
 * ---------------- Arithmetic Operators -------------------
 *  + , - , * , / , %(modulus=remainder) , ++ , -- , ** 
 */


 let num_1 = 10;
 let num_2 = 20;

 let product = num_1 + num_2;
 console.log("Addition "+product);

product = num_1 - num_2;
 console.log("Substraction "+product);


product = num_1 / num_2;
 console.log("Division "+product);


product = num_1 * num_2;
 console.log("Multiplication "+ product);

product = num_1 % num_2;
 console.log("Modulus "+product);









