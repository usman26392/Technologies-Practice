/**
 * Arrow function
 */


 // function expression
 let myFunc_1 = function show1() {
     console.log("This is javaScript");
 };
 myFunc_1();

 // Anonymous function
 let myFunc_2 = function() {
    console.log("This is javaScript");
};
myFunc_2();

// Arrow function
let myFunc_3 = () => {
    console.log("This is javaScript");
};
myFunc_3();
